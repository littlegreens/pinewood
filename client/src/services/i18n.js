const ITALIAN_PREFIX = "it";

export function detectLanguage() {
  const browserLang =
    localStorage.getItem("pinewood_lang") ||
    navigator.language ||
    navigator.userLanguage ||
    "en";

  return browserLang.toLowerCase().startsWith(ITALIAN_PREFIX) ? "it" : "en";
}

